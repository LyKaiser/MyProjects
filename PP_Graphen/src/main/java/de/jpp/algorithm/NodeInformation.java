package de.jpp.algorithm;

import de.jpp.model.interfaces.Edge;

public class NodeInformation<N,A> {
    private Edge<N,A> predecssor;
    private double distance;

    public NodeInformation(Edge<N, A> predecssor, double distance) {
        this.predecssor = predecssor;
        this.distance = distance;
    }

    public Edge<N, A> getPredecssor() {
        return predecssor;
    }

    public void setPredecssor(Edge<N, A> predecssor) {
        this.predecssor = predecssor;
    }

    public double getDistance() {
        return distance;
    }

    public void setDistance(double distance) {
        this.distance = distance;
    }
}
