package regEx;

public class Apartment {

  private String city;

  private Double livingSpace;

  private Double numRooms;

  private Double price;
  
  private String phoneNumber;

  public Apartment() {
  }

  public Apartment(String city, Double livingSpace, Double numRooms, Double price, String phoneNumber) {
    this.city = city;
    this.livingSpace = livingSpace;
    this.numRooms = numRooms;
    this.price = price;
    this.phoneNumber = phoneNumber;
  }

  public String getCity() {
    return city;
  }

  public void setCity(String city) {
    this.city = city;
  }

  public Double getLivingSpace() {
    return livingSpace;
  }

  public void setLivingSpace(Double livingSpace) {
    this.livingSpace = livingSpace;
  }

  public Double getNumRooms() {
    return numRooms;
  }

  public void setNumRooms(Double numRooms) {
    this.numRooms = numRooms;
  }

  public Double getPrice() {
    return price;
  }

  public void setPrice(Double price) {
    this.price = price;
  }

  public String getPhoneNumber() {
    return phoneNumber;
  }

  public void setPhoneNumber(String phoneNumber) {
    this.phoneNumber = phoneNumber;
  }

  @Override
  public String toString() {
    return "Apartment{" +
            "city='" + city + '\'' +
            ", livingSpace=" + livingSpace +
            ", numRooms=" + numRooms +
            ", price=" + price +
            ", phoneNumber='" + phoneNumber + '\'' +
            '}';
  }
}
